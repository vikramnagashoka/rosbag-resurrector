"""Auto-generate a README.md for exported datasets.

Creates a self-documenting directory with bag sources, topic list,
sync config, health scores, and a Python snippet to load the data.
"""

from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


def generate_dataset_readme(
    output_path: Path,
    dataset_name: str,
    version: str,
    config: dict[str, Any],
    metadata: dict[str, Any],
    manifest: dict[str, str],
) -> Path:
    """Generate a README.md in the export directory.

    Args:
        output_path: Directory containing exported files.
        dataset_name: Name of the dataset.
        version: Version string.
        config: Dataset config dict.
        metadata: Dataset metadata dict.
        manifest: File manifest {filename: sha256}.

    Returns:
        Path to the generated README.md.
    """
    bag_refs = config.get("bag_refs", [])
    topics = config.get("topics", [])
    sync_cfg = config.get("sync_config")
    export_format = config.get("export_format", "parquet")
    downsample_hz = config.get("downsample_hz")

    # Determine file listing
    data_files = [f for f in manifest.keys() if not f.endswith(".json")]

    lines = [
        f"# {dataset_name} (v{version})",
        "",
        f"> Generated by [RosBag Resurrector](https://github.com/vikramnagashoka/rosbag-resurrector) "
        f"on {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}",
        "",
    ]

    # Description
    if metadata.get("description"):
        lines.extend([metadata["description"], ""])

    # Metadata table
    meta_rows = []
    if metadata.get("robot_type"):
        meta_rows.append(f"| Robot | {metadata['robot_type']} |")
    if metadata.get("task"):
        meta_rows.append(f"| Task | {metadata['task']} |")
    if metadata.get("environment"):
        meta_rows.append(f"| Environment | {metadata['environment']} |")
    if metadata.get("license"):
        meta_rows.append(f"| License | {metadata['license']} |")
    if metadata.get("tags"):
        meta_rows.append(f"| Tags | {', '.join(metadata['tags'])} |")

    if meta_rows:
        lines.extend([
            "## Metadata",
            "",
            "| Field | Value |",
            "|-------|-------|",
            *meta_rows,
            "",
        ])

    # Source bags
    lines.extend(["## Source Bags", ""])
    for ref in bag_refs:
        path = ref.get("path", "?")
        line = f"- `{path}`"
        if ref.get("start_time") or ref.get("end_time"):
            line += f" (slice: {ref.get('start_time', '0')} → {ref.get('end_time', 'end')})"
        if ref.get("topics"):
            line += f" — topics: {', '.join(ref['topics'])}"
        lines.append(line)
    lines.append("")

    # Topics
    if topics:
        lines.extend([
            "## Topics",
            "",
            "| Topic |",
            "|-------|",
            *[f"| `{t}` |" for t in topics],
            "",
        ])

    # Configuration
    lines.extend(["## Configuration", ""])
    lines.append(f"- **Export format**: `{export_format}`")
    if sync_cfg:
        lines.append(f"- **Sync method**: `{sync_cfg['method']}`")
        lines.append(f"- **Sync tolerance**: `{sync_cfg['tolerance_ms']}ms`")
        if sync_cfg.get("anchor"):
            lines.append(f"- **Anchor topic**: `{sync_cfg['anchor']}`")
    if downsample_hz:
        lines.append(f"- **Downsampled to**: `{downsample_hz}Hz`")
    lines.append("")

    # Files
    if data_files:
        total_size = sum(
            (output_path / f).stat().st_size
            for f in data_files
            if (output_path / f).exists()
        )
        lines.extend([
            "## Files",
            "",
            f"Total size: **{_format_size(total_size)}**",
            "",
            "| File | SHA256 (first 12) |",
            "|------|-------------------|",
        ])
        for f in sorted(data_files):
            sha = manifest.get(f, "?")[:12]
            lines.append(f"| `{f}` | `{sha}...` |")
        lines.append("")

    # Quick start code snippet
    lines.extend([
        "## Quick Start",
        "",
        "```python",
    ])

    if export_format == "parquet":
        lines.extend([
            "import polars as pl",
            "",
            "# Load all parquet files",
            f'df = pl.read_parquet("{output_path}/*.parquet")',
            "print(df.head())",
        ])
    elif export_format == "hdf5":
        lines.extend([
            "import h5py",
            "",
            f'with h5py.File("{output_path}/{data_files[0] if data_files else "data.h5"}", "r") as f:',
            '    for key in f.keys():',
            '        print(key, f[key].shape)',
        ])
    elif export_format == "csv":
        lines.extend([
            "import polars as pl",
            "",
            f'df = pl.read_csv("{output_path}/{data_files[0] if data_files else "data.csv"}")',
            "print(df.head())",
        ])
    elif export_format == "numpy":
        lines.extend([
            "import numpy as np",
            "",
            f'data = np.load("{output_path}/{data_files[0] if data_files else "data.npz"}")',
            "print(list(data.keys()))",
        ])
    else:
        lines.append(f"# Load your {export_format} files from this directory")

    lines.extend([
        "```",
        "",
        "---",
        f"*Reproducible dataset — see `manifest.json` for file checksums and `dataset_config.json` for full configuration.*",
    ])

    readme_path = output_path / "README.md"
    readme_path.write_text("\n".join(lines))
    return readme_path


def _format_size(size_bytes: int) -> str:
    for unit in ("B", "KB", "MB", "GB", "TB"):
        if size_bytes < 1024:
            return f"{size_bytes:.1f} {unit}"
        size_bytes /= 1024
    return f"{size_bytes:.1f} PB"
